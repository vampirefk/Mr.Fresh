package fresh;

public class Good {
	private String goodname;
	private int num;
	private float price;
	private byte pic;
	
	public String getGoodname() {
		return goodname;
	}
	public void setGoodname(String n) {
		goodname=n;
	}
	
	public int getNum() {
		return num;
	}
	public void setNum(int n) {
		num=n;
	}
	
	public float getPrice() {
		return price;
	}
	public void setPrice(float f) {
		price=f;
	}
	
	public byte getPic() {
		return pic;
	}
	public void setPic(byte p) {
		pic=p;
	}
}
